                            Course Progress Requirements
                            </a>
                            <i class="fa fa-plus" aria-hidden="true"></i>
                        </div>
                        <div class="collapse" id="faq16" data-parent="#accordion">
                          <div class="answer">
                                <p>
                                Under the ESOS Act, the National Code of Practice for Providers of Education and Training to Overseas Students 2018 (National Code 2018) – Standard 8 Overseas student Visa Requirements
                                Australian International Institute of Technology will:
                                </p>
                                <ol class="px-3">
                                <li><p>Monitor the course progress and attendance of each overseas student to ensure the overseas student is in a position to complete their VET course within the expected duration that is specified on the overseas student’s CoE:</p>
                                      <li><p>This is the responsibility of each Trainer assigned a timetabled VET group of International students.</p></li>
                                      <li><p>When monitoring their students (using an Excel spreadsheet), the Trainer will take into account each student’s attendance and their current course progress in completing their course of studies within the enrolment period of their CoE. Specifically, if a student does not complete a Unit of Competency within the timetabled period for that Unit of Competency, the Trainer will automatically determine that the student is unlikely to complete their course of studies within the enrolment period of their CoE. Accordingly, the Trainer will evoke Australian International Institute of Technology’s student at risk intervention strategy with the particular student; which they will document and place on the student’s academic file; utilising the re-assessment and catch-up days in the timetable for this purpose (also recording the Intervention on their Excel spreadsheet for that student).</p></li>
                                      <li><p>The Trainer will continue to monitor the student’s course progress and attendance and if they believe, following the intervention, that the International student continues to be at risk of not making satisfactory course progress, the Trainer will arrange for Australian International Institute of Technology to inform the student in writing of its intention to report the student and that they are able to access Australian International Institute of Technology’s appeal process within 20 working days.</p></li>
                                      <li><p>At the end of this period, after the appeals process (if actioned) is finalised and upholds Australian International Institute of Technology’s decision to report, Australian International Institute of Technology will notify the Secretary of the Department of Education through PRISMS of the student not achieving satisfactory progress.</p></li>
                                      <li><p>Where the student is reported to the Department of Education via PRISMS, Australian International Institute of Technology will maintain a copy of this report on the student’s file.</p></li>
                                      <li><p>Australian International Institute of Technology may decide not to report the overseas student if the student is maintaining satisfactory course progress.</p></li>
                                    </ol>
                          </div>
